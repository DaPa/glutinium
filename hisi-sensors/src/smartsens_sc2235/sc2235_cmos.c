HI_VOID cmos_set_pixel_detect(HI_BOOL bEnable)
{
    //min fps is 8.23,so u32FullLines_5Fps = FULL_LINES_MAX
    HI_U32 u32FullLines_5Fps, u32MaxIntTime_5Fps;

    if (SC2235_SENSOR_1080P_30FPS_LINEAR_MODE == gu8SensorImageMode)
    {
        u32FullLines_5Fps = FULL_LINES_MAX;
    }
	else
    {
        return;
    }
    //u32FullLines_5Fps = (u32FullLines_5Fps > FULL_LINES_MAX) ? FULL_LINES_MAX : u32FullLines_5Fps;
    u32MaxIntTime_5Fps = u32FullLines_5Fps;
	
    if (bEnable) /* setup for ISP pixel calibration mode */
    {
		sensor_write_register(VMAX0_ADDR, (u32FullLines_5Fps & 0xFF00) >> 8);  /* 5fps */
        sensor_write_register(VMAX1_ADDR, u32FullLines_5Fps & 0xFF);           /* 5fps */
        sensor_write_register(EXPTIME0_ADDR, ((u32MaxIntTime_5Fps >> 4) & 0xFF));   /* max exposure lines */
        sensor_write_register(EXPTIME1_ADDR, ((u32MaxIntTime_5Fps << 4) & 0xF0));   /* max exposure lines */
		sensor_write_register(GAIN0_ADDR, 0x00);                                    /* min Gain = 1*/
		sensor_write_register(GAIN1_ADDR, 0x03);
        sensor_write_register(GAIN2_ADDR, 0x10);
    }
    else /* setup for ISP 'normal mode' */
    {
        gu32FullLinesStd = (gu32FullLinesStd > FULL_LINES_MAX) ? FULL_LINES_MAX : gu32FullLinesStd;
        gu32FullLines = gu32FullLinesStd;		
		sensor_write_register(VMAX0_ADDR, ((gu32FullLinesStd & 0xFF00) >> 8));  /* Standard FPS */
		sensor_write_register(VMAX1_ADDR, (gu32FullLinesStd & 0xFF));           /* Standard FPS */
		bInit = HI_FALSE;
    }

    return;
}

